/**
 * Shared UI constants for consistent layout behavior across tables, grids,
 * and other scrollable data surfaces.
 */

/** Minimum width for data tables before horizontal scrolling kicks in.
 *  Tables wider than this will scroll horizontally on narrow viewports.
 */
export const TABLE_MIN_WIDTH = 860;

/** Default maximum height for scrollable table containers inside cards. */
export const TABLE_MAX_HEIGHT = 600;
